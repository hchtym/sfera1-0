char* deviceControler::formatAmount(unsigned long a, char *temp)
{
    unsigned int i,j;
    char amount[32];
    
    sprintf(amount,"%ld",a );
    
    temp[0] = 0;

    if(strlen(amount)<4){
        strcpy(temp, amount);
        return temp;
    }
    // pierwszy separator
    i = strlen(amount) % 3;
  
    if(i!=0){
        strncat(temp, amount, i);
        strcat(temp, " ");
    }
    
    for(j=0; j< strlen(amount) / 3;  j++){
        strncat(temp, &amount[j*3]+i, 3);
        if(j < strlen(amount)/3 -1)
            strcat(temp, " ");
    }
    return temp;
}

